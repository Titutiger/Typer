def create_word_map(words_with_forms, key_length=3):
    """
    Create mapping dictionary with chords pointing to lists of word forms.
    For normal words, list has single word; for verbs, list contains forms in specified order.
    """
    word_map = {}
    for word, forms in words_with_forms.items():
        unique_letters = []
        for ch in word.lower():
            if ch not in unique_letters:
                unique_letters.append(ch)
            if len(unique_letters) == key_length:
                break
        chord_key = frozenset(unique_letters)
        word_map[chord_key] = forms
    return word_map

