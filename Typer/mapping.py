WORD_MAP = create_word_map(WORDS_WITH_FORMS)

def get_word_forms_from_chord(chord_keys):
    """
    Given chord keys, return list of word forms if found, else None.
    """
    filtered = set()
    for c in sorted(chord_keys):
        if len(filtered) < 3:
            filtered.add(c)
        else:
            break
    chord_key = frozenset(filtered)
    return WORD_MAP.get(chord_key, None)
